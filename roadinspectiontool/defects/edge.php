			<h4>Description:</h4> 
 
Edge damage is a difference in elevation between the pavement edge and the shoulder and occurs 
along the edge of the pavement. 
<h4>Possible Causes: </h4>
 
-  Wear of the shoulder (formation of step). 
-  Action of water. 
-  Insufficient compaction of the edges of bituminous pavements. 
-  Road is too narrow. 
-  Excessive axle loads. 
 
<h4>Effect (if neglected):<h4> 
 
-  Rapid deterioration during the rainy season. 
-  Traffic action will worsen the defect
<h4>Inspection Reporting Method: </h4>
 
-  Scaling % of sub-section length and average depth. 
<h4>Inspection Tools: </h4>
 
-  Tape
