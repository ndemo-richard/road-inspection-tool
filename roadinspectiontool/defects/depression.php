			<h4>Description: </h4>
 
Any deviation of surface from its original shape (other than described for corrugation or rutting). 
Depressions usually take the shape of dishing, bumps or dips which are noticeable, all of which give 
rise to pitch, roll, or jarring drop in a moving vehicle. 
 
<h4>Possible Causes: </h4>
 
-  Differential settlement of subgrade or base materials. 
-  Lack of subgrade support. 
-  Embankment slope failure. 
 
<h4>Effect (if neglected): </h4>
 
-  Water ponding in the depression can penetrate the lower layers and cause softening and potential 
early failure. 
-  Depressions may expand into cracking and potholes. 
 
<h4>Inspection Reporting Method: </h4>
 
-  Scaling % of sub-section length. 
 
