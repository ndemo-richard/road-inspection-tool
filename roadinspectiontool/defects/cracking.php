			<h4>Possible Causes:</h4> 
 
<h5>Alligator</h5> 
-  Inadequate pavement thickness. 
-  Low modulus base. 
-  Brittle base or wearing course (e.g. cemented, aged). 
-  Fatigue failure of the asphalt pavement under repeated traffic loading. 
 
<h5>Transverse </h5>
-  Reflection of shrinkage crack or joint in an underlying base (common with portland cement 
concrete or cemented materials). 
-  Construction joint or shrinkage crack (due to low temperature or bitumen hardening) in asphalt 
surfacing. 
-  Structural failure of portland cement concrete base. 
 
<h5>Longitudinal</h5> 
-  Reflection of shrinkage crack or joint in an underlying base (common with portland cement 
concrete, cemented base of asphalt surfacing). 
-  Poorly constructed paving lane joint in asphalt surfacing. 
-  Daily temperature cycles or asphalt hardening. 
-  Displacement of joint at pavement widening. 
-  Volume change of expansive clay sub-grade. 
-  Cyclical weakening of pavement edge. 
-  Differential settlement between cut and fill. 
 
 
<h4>Effect (if neglected): </h4>
 
-  Allows water to enter the pavement causing softening and weakening of the pavement and 
lower layers. This may cause early failure of the pavement. 
-  If severe, causes uneven ride for the motorist and may reduce traffic speed.  
 
<h4>Inspection Reporting Method: </h4>
 
-  Scaling % of sub-section length. 
 
