			<h4>Description: </h4>
 
Vegetation growth overruns shoulder and reaches or approaches roadway edge. 
 
<h4>Possible Causes: </h4>
 
- Grass, weeds, brushes or trees have been allowed to grow unchecked. 
 
<h4>Effect (if neglected):</h4> 
 
- Surface water can pond at the edge of the carriageway and weaken the pavement. 
- Silt accumulates at the edge of the carriageway. 
- The visibility for road users is reduced, with increased risk of accidents. 
- Increased fire hazard in the dry season. 
 
<h4>Inspection Reporting Method: </h4>
 
- Defect ranking. 
 
