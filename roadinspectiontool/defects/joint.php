			<h4>Description:</h4> 
 
The vertical movement of the concrete on one side of a joint relative to that on the other side of the 
joint. 
<h4>Possible Causes: </h4>
 
-  Insufficient compaction of subgrade/base course. 
-  Differential settlement of ground. 
-  Due to pumping action. 
-  Inadequate function of dowel bar and/or tie-bar. 
-  Water penetration of the joint causing softening of the supporting layers.
<h4>Effect (if neglected): </h4>
 
-  Increased severity of settlement causing a hazard to traffic. 
-  Increased water penetration causing softening and erosion of the supporting layers. 
-  Cracking of concrete adjacent to the joint. 
<h4>Inspection Reporting Method:</h4> 
 
-  Defect ranking.
 
