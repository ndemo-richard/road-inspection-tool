			<h4>Description: </h4>
 
Surface texture is worn down and stripped away by traffic.
  <h4>Possible Causes: </h4>
 
- Using soft aggregate.  
-  Inadequate construction work (e.g., compacting was not enough, wrong concrete mix, wrong 
grooving depth or type of grooving).  
<h4>Effect (if neglected): </h4>
 
-  Loss of friction grip for vehicle tyres creating a safety risk. 
-  Water is unable to disperse from the carriageway along the grooves creating standing water on the 
carriageway and a serious accident hazard. 
<h4>Inspection Reporting Method:</h4> 
 
-  Scaling % of sub-section length. 
