			<h4>Description: </h4>
 
Bowl-shaped depressions in the road surface which can be oval, or irregular in shape. 
 
 
<h4>Possible Causes: </h4>
 
-  Ponding of water. 
-  Excessive weakening of layer by moisture and/or traffic action. 
-  Inadequate initial compaction. 
-  Variable quality of materials. 
-  Insufficient structural thickness. 
-  Excessive axle loads. 
 
 
<h4>Effect (if neglected): </h4>
 
-  If left unrepaired, can rapidly expand the extent of the damage. 
-  Creates poor ride quality for the motorist and may reduce traffic speed. 
-  If large can cause damage to vehicle wheels and tyres. 
-  Can create an accident risk. 
 
 
<h4>Inspection Reporting Method: </h4>
 
- Scaling length, width, depth and number of potholes. 
 
 
