		  <h4>Description:</h4> 
 
A series of closely-spaced crest and valleys in the form of corrugation. The corrugation resembles an 
old fashioned “washboard” surface. Corrugations always occur with the ripples perpendicular to the 
direction of travel and more pronounced at the wheelpaths, although it can and sometimes does cover 
the whole pavement surface area. 
 
 
<h4>Possible Causes:</h4> 
 
-  Insufficient pavement structural thickness (pavement strength) 
-  Inadequate quality of base material for prevailing climatic and traffic conditions.  Most common in    
dry conditions. 
-  The weeds. 
 
 
<h4>Effect (if neglected):</h4> 
 
-  Vehicle jumps as it travels over the corrugations. 
-  Vehicle slips sideways and driver must maintain firm steering control. 
-  Generally reduces vehicle travel speed.  
 
 
<h4>Inspection Reporting Method:</h4> 
 
-  Defect ranking. 
 
 
<h4>Inspection Tools: </h4>
 
-  Nil.
