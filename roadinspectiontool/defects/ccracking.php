			<h4>Description: </h4>
 
Longitudinal or transverse cracks in the pavement surface
<h4>Possible Causes: </h4>
 
-  Excessive dry condition during the construction work. 
-  Bearing capacity of subgrade/sub-base is not enough. 
-  Inadequate structure/function of joint. 
-  Thickness of concrete slab is not enough. 
-  Poor quality of concrete. 
-  Differential settlement of ground. 
-  Excessive axle loads. 
-  Underground utility services (e.g., water pipe leakage, electrical cable etc.
<h4>Effect (if neglected): </h4>
 
-  Increased extent or severity of cracking causing potential premature pavement failure. 
-  Water penetration causing softening of lower layers and potential pavement failure.
<h4>Inspection Reporting Method:</h4> 
 
-  Scaling % of sub-section length.
<h4>Inspection Tools: </h4>
 
-  Tape
