			<h4>Description: </h4>
 
Bowl-shaped depressions in the surface oval, or irregular in shape.  
 
<h4>Possible Causes: </h4>
 
-  Ponding of water. 
-  Excessive weakening of layer by moisture and traffic action. 
-  Inadequate initial compaction. 
-  Variable quality of materials. 
-  Insufficient shoulder depth. 
 
<h4>Effect (if neglected): </h4>
 
-  If left unrepaired, can rapidly expand the extent of the damage. 
-  Standing water can penetrate the lower layers causing early failure. 
-  Failure of the shoulder could lead to pavement edge failure. 
 
<h4>Inspection Reporting Method: </h4>
 
-  Scaling length, with, depth and number of potholes. 
 
