			<h4>Description: </h4>
 
Bleeding is a film of bituminous material on the  pavement surface which creates a shiny, glass-
excessive amount of asphalt cement in the mix and/or low air void content. It occurs when asphalt 
fills the void of the mix during hot weather and then expands into the pavement surface.
         <h4>Possible Causes:</h4> 
 
- Excessive application rate of binder, with respect to stone size. 
- Excessive prime coat being incorporated into the seal. 
- Excessive binder in underlying surface (patch or flushed area). 
- Primer seal covered before volatiles in primer binder have evaporated
  <h4>Effect (if neglected): </h4>
 
- The road surface becomes slippery when wet and hazardaous to traffic. 
- Separation and break-away of surface layer under the action of traffic. 
<h4>Inspection Reporting Method: </h4>
 
- Defect ranking.
