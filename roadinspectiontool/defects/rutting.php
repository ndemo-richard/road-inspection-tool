			  <h4>Description:</h4> 
 
					Rutting is characterised by longitudinal depressions in the pavement surface that occur in the wheel 
					paths of a roadway
					<h4>Possible Causes:</h4> 
 
-  Inadequate pavement thickness. 
-  Inadequate compaction in surfacing or base. 
-  Inadequate strength (stability) in surfacing or base. 
-  Excessive bitumen in the mix. 
-  Excessive axle loads.
               <h4> Effect (if neglected):</h4> 
 
-  If water is able to penetrate into the body of the pavement, then there will be a rapid increase in 
the degree of rutting often leading to cracking and break-up of the pavement. 
-  If excessive can reduction in serviceability and reduce vehicle travel speeds and in very severe 
cases, may be an accident risk.  
                <h4>Inspection Reporting Method:</h4> 
 
-  Scaling average depth. 
