		<h4>Description: </h4>
 
Localized softening of the running surface. Subgrade soils may be punched up through a gravel 
surface, usually with the broken surface are surrounded by depression or dishing type of distortion. 
Distress most likely to occur in the wheel tracks. 
 
 
<h4><Possible Causes: </h4>
 
-  Caused by the sheer failure of the base of subgrade soil. 
-  Traffic action combined with excessive moisture from improperly drained surface. 
-  Poor quality materials. 
-  Lack of compaction. 
 
 
<h4>Effect (if neglected):</h4> 
 
-  Road becomes impassable to traffic. 
-  Soft spot extends to require major reconstruction. 
 
 
<h4>Inspection Reporting Method: </h4>
 
-  Defect ranking. 
 
 
<h4>Inspection Tools: </h4>
 
-  Nil. 
