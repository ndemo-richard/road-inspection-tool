			<h4>Description:</h4> 
 
Stripping/Fretting is the wearing away of the pavement surface caused by the dislodging of 
aggregate particles and loss of asphalt binder. This generally indicates that the asphalt binder has 
hardened significantly. Removal of the coarse aggregate of a sprayed seal leaving the binder 
exposed to tyre contact – can happen as the loss of individual stones, or as the complete loss of 
stone in a localised area. 
            <h4>Possible Causes: </h4>
 
- Low binder contents 
- Poor binder to stone adhesion (dirty or hydropholic aggregates, without effective precoating with 
adhesion agent or wet stone etc.)  
- Ageing or absorption of binder. 
- Stone deterioration. 
- Incorrect blending of binder. 
- Inadequate rolling before opening the seal to traffic. 
- Poor workmanship.
   <h4>Effect (if neglected): </h4>
 
- Progressive break-away of chippings resulting in the surfacing becoming more slippery, more 
permeable, or worn out by traffic. 
<h4>Inspection Reporting Method: </h4>
 
- Scaling of sub-section length.
<h4>Inspection Tools:</h4> 
 
- Tape 
