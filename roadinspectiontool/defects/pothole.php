			<h4>Description:</h4> 
 
Potholes are small bowl-shaped depressions on  the pavement surface/base course, usually less 
than one metre in diameter. They generally have sharp edges and vertical sides near the top of the 
hole. Their growth is accelerated by free water collecting inside the hole. Potholes are reproduced 
when traffic abrades small pieces of pavement surface. The pavement then continues to 
disintegrate because of poor surface quality, weak spots in the base or subgrade, or because it has 
reached a condition of high-severity alligator cracking. Most potholes are structurally related 
distresses ad should not be confused with ravelling and weathering. When holes are created by 
high-severity alligator cracking, they should be identified as potholes.
           <h4> Possible Causes: </h4>
 
-  Asphalt content too low. 
-  Excessive heating of asphalt. 
-  Poor quality mixture. 
-  Lack of compaction allowing ingress of water. 
-  Excessive axle loads. 
-  Mechanical damage to the road due to poor reinstatement of roads after services installations. 
-  Injury to pavement. 
-  Spills or leakages. 
          <h4> Effect (if neglected): </h4>
 
-  Allows water to enter the pavement causing softening and weakening of the pavement and 
lower layers. This may cause early failure of the pavement. 
-  If left unrepaired, can rapidly expand the extent of the damage. 
-  Creates poor ride quality for the motorist and may reduce traffic speed. 
-  If large can cause damage to vehicle wheels and tyres. 
-  Can create an accident risk. 
            <h4>Inspection Reporting Method: </h4>
 
-  Scaling length, width, depth and number of potholes.
