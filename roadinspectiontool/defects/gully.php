			<h4>Description: </h4>
 
Erosion gullies are longitudinal depressions in the road surface that normally occur in the wheel paths 
and the sides of the roadway. 
 
 
<h4>Possible Causes: </h4>
 
-  Rain water running along the wheel paths. 
-  Poor transversal drainage or high road gradient. 
-  Sewage/Pipe water leaks at urban areas. 
 
 
<h4>Effect (if neglected): </h4>
 
-  Ponding of water in the gullies softening the pavement and making the road impassable. 
-  Reduced vehicle travel speed. 
 
 
<h4>Inspection Reporting Method:</h4> 
 
-  Scaling % of sub-section length and average depth. 
 
 
<h4>Inspection Tools: </h4>
 
-  Tape 
