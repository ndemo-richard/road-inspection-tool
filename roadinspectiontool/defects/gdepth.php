		<h4> Description: </h4>
 
Gravel in windrows along the centre of the road alongside the wheel tracks, or along the shoulder, all 
parallel to the direction of traffic. Traffic action also segregates coarse aggregates from the fine 
particles and then moves loosened aggregates away from the wheel tracks to form windrows parallel to 
the direction of traffic over the less travelled roadway.  
 
<h4>Possible Causes: </h4>
 
-  Loose compaction at construction or regravelling. 
-  Segregation of coarse aggregate from the fine particles by traffic action. 
 
<h4>Effect (if neglected): </h4>
 
-  Road is closed to traffic. 
-  Softening of surface making it impassable to traffic. 
 
<h4>Inspection Reporting Method: </h4>
 
-  Scaling % of sub-section length. 
 
<h4>Inspection Tools: </h4>
 
-  Tape
