			<h4>Description: </h4>
 
Rutting is characterised by a longitudinal depression in the road surface that occur in the wheelpaths of 
a roadway
           <h4> Possible Causes:</h4> 
 
-  Inadequate wet strength of subgrade. 
-  Wear by attrition due to traffic or erosion of surface material. 
-  Excessive loose material. 
-  Traffic compaction of subgrade. 
-  Excessive axle loads.
<h4>Effect (if neglected): </h4>
 
-  Causes poor ride quality. 
-  Water can collect in rut and cause aquaplaning problem for vehicles. 
<h4>Inspection Reporting Method: </h4>
 
-  Defect ranking. 
 
 
